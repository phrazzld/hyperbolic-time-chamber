set -euo pipefail

# Colors for output
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[0;33m'
BLUE='\033[0;34m'
BOLD='\033[1m'
NC='\033[0m' # No Color

# Configuration
FASTLANE_DIR="fastlane"
CERTS_DIR="certs"

# Helper functions
log_info() {
    echo -e "${BLUE}üöÄ $1${NC}"
}

log_success() {
    echo -e "${GREEN}‚úÖ $1${NC}"
}

log_warning() {
    echo -e "${YELLOW}‚ö†Ô∏è  $1${NC}"
}

log_error() {
    echo -e "${RED}‚ùå $1${NC}"
}

log_header() {
    echo ""
    echo -e "${BOLD}${BLUE}=== $1 ===${NC}"
    echo ""
}

# Check if required tools are available
check_dependencies() {
    log_info "Checking deployment setup dependencies..."
    
    # Check for Xcode
    if ! command -v xcodebuild &> /dev/null; then
        log_error "Xcode command line tools not found. Please install Xcode."
        exit 1
    fi
    
    # Check for git
    if ! command -v git &> /dev/null; then
        log_error "Git not found. Please install Git."
        exit 1
    fi
    
    # Check for Ruby (for Fastlane)
    if ! command -v ruby &> /dev/null; then
        log_error "Ruby not found. Please install Ruby."
        exit 1
    fi
    
    log_success "All required dependencies available"
}

